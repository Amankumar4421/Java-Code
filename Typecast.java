public class Typecast {
    public static void main(String[] args) {
        double d=48.5463;
        byte b=(byte)d;
        short s=(short)d;
        int i=(int)d;
        long l=(long)d;
        float f=(float)d;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
    }
    
}
